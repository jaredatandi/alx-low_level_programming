#include "3-calc.h"

/**
 * op_add - adds the arguments
 * @a: the first argument
 * @b: the second argument
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts the arguments
 * @a: the first argument
 * @b: the second argument
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies the arguments
 * @a: the first argument
 * @b: the second argument
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides the  arguments
 * @a: the first argument
 * @b: the second argument
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the modulus of the arguments
 * @a: the first argument
 * @b: the second argument
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
